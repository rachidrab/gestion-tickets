    @Test
    @Transactional
    void putNewTicket() throws Exception {
        // Initialize the database
        ticketRepository.saveAndFlush(ticket);

        int databaseSizeBeforeUpdate = ticketRepository.findAll().size();

        // Update the ticket
        Ticket updatedTicket = ticketRepository.findById(ticket.getId()).get();
        // Disconnect from session so that the updates on updatedTicket are not directly saved in db
        em.detach(updatedTicket);
        updatedTicket
            .dateOuverture(UPDATED_DATE_OUVERTURE)
            .dateCloture(UPDATED_DATE_CLOTURE)
            .statut(UPDATED_STATUT)
            .descripition(UPDATED_DESCRIPITION)
            .urgence(UPDATED_URGENCE)
            .environnement(UPDATED_ENVIRONNEMENT);

        restTicketMockMvc
            .perform(
                put(ENTITY_API_URL_ID, updatedTicket.getId())
                    .contentType(MediaType.APPLICATION_JSON)
                    .content(TestUtil.convertObjectToJsonBytes(updatedTicket))
            )
            .andExpect(status().isOk());

        // Validate the Ticket in the database
        List<Ticket> ticketList = ticketRepository.findAll();
        assertThat(ticketList).hasSize(databaseSizeBeforeUpdate);
        Ticket testTicket = ticketList.get(ticketList.size() - 1);
        assertThat(testTicket.getDateOuverture()).isEqualTo(UPDATED_DATE_OUVERTURE);
        assertThat(testTicket.getDateCloture()).isEqualTo(UPDATED_DATE_CLOTURE);
        assertThat(testTicket.getStatut()).isEqualTo(UPDATED_STATUT);
        assertThat(testTicket.getDescripition()).isEqualTo(UPDATED_DESCRIPITION);
        assertThat(testTicket.getUrgence()).isEqualTo(UPDATED_URGENCE);
        assertThat(testTicket.getEnvironnement()).isEqualTo(UPDATED_ENVIRONNEMENT);
    }

